
    function changeMobileCover11(imageName) {
        const mainImage = document.getElementById('mobile-cover-main-img1');
        mainImage.src = imageName
    }
    function inc11() {
        const a = document.getElementById("numchange1")
        const qnt = parseInt(a.innerHTML)
        if (qnt == 10) {
            alert("Maximum Quantity of product is 10")
        }
        else {
            a.innerHTML = qnt + 1;
        }
    
    }
    function dec11() {
        const a = document.getElementById("numchange1")
        const qnt = parseInt(a.innerHTML)
        if (qnt == 1) {
            alert("Minimum Quantity of product is 1")
        }
        else {
            a.innerHTML = qnt - 1;
        }
    
    }
